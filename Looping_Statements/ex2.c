//2. Write a C program to pront all natural numbers in reverse(From n to 1).
//---Using while loop
#include<stdio.h>
void main()
{
	int n;
	printf("Enter the number n");
	scanf("%d\n",&n);
	while(n>=1)
	{
		printf("%d\n",n);
		n--;
	}
	printf("\n");
}




















































































